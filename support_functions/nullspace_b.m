function [ null_b ] = nullspace_b( b )
    if b(3) >= 0
        null_b = [1-(b(1)*b(1))/(1+b(3)),  -(b(1)*b(2))/(1+b(3)); ...
                   -(b(1)*b(2))/(1+b(3)), 1-(b(2)*b(2))/(1+b(3)); ...
                   -b(1)            ,  -b(2)]';
    else 
        null_b = [1-(b(1)*b(1))/(1-b(3)),  -(b(1)*b(2))/(1-b(3)); ...
                   -(b(1)*b(2))/(1-b(3)), 1-(b(2)*b(2))/(1-b(3)); ...
                    b(1)            ,   b(2)]';
    end
end

